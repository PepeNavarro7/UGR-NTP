package base.inicializacion;

/**
 * enumerado para los modos de inicializacion
 */
public enum ModoInicializacion {
   /**
    * muestreo estratificado para seleccion de centros
    */
   MUESTREO_UNIFORME,

   /**
    * muestreo aleatorio para seleccion de centros
    */
   MUESTREO_ALEATORIO,

   /**
    * seleccion uniforme en el rango de colores
    */
   SELECCION_UNIFORME
}
